def add(*args):
    total = 0
    for i in args:
        total += i
    return total

def subtract(smallNumber,largeNumber):
    difference = 0
    difference = largeNumber - smallNumber
    return difference

def multiply(*args):
    product = 1
    for i in args:
        product *= i
    return product

def divide(dividend, divisor):
    quotient = dividend/divisor
    return quotient

